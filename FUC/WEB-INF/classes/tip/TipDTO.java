package tip;

public class TipDTO {
	
	String userID;
	int tipID;
	String tipTitle;
	String tipContent;
	String tipDate;
	String tipLang;
	int tipHit;
	String tipFile;
	String tipRealFile;
	int tipGroup;
	int tipSequence;
	int tipLevel;
	int tipAvailable;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getTipID() {
		return tipID;
	}
	public void setTipID(int tipID) {
		this.tipID = tipID;
	}
	public String getTipTitle() {
		return tipTitle;
	}
	public void setTipTitle(String tipTitle) {
		this.tipTitle = tipTitle;
	}
	public String getTipContent() {
		return tipContent;
	}
	public void setTipContent(String tipContent) {
		this.tipContent = tipContent;
	}
	public String getTipDate() {
		return tipDate;
	}
	public void setTipDate(String tipDate) {
		this.tipDate = tipDate;
	}
	public String getTipLang() {
		return tipLang;
	}
	public void setTipLang(String tipLang) {
		this.tipLang = tipLang;
	}
	public int getTipHit() {
		return tipHit;
	}
	public void setTipHit(int tipHit) {
		this.tipHit = tipHit;
	}
	public String getTipFile() {
		return tipFile;
	}
	public void setTipFile(String tipFile) {
		this.tipFile = tipFile;
	}
	public String getTipRealFile() {
		return tipRealFile;
	}
	public void setTipRealFile(String tipRealFile) {
		this.tipRealFile = tipRealFile;
	}
	public int getTipGroup() {
		return tipGroup;
	}
	public void setTipGroup(int tipGroup) {
		this.tipGroup = tipGroup;
	}
	public int getTipSequence() {
		return tipSequence;
	}
	public void setTipSequence(int tipSequence) {
		this.tipSequence = tipSequence;
	}
	public int getTipLevel() {
		return tipLevel;
	}
	public void setTipLevel(int tipLevel) {
		this.tipLevel = tipLevel;
	}
	public int getTipAvailable() {
		return tipAvailable;
	}
	public void setTipAvailable(int tipAvailable) {
		this.tipAvailable = tipAvailable;
	}
	
}
